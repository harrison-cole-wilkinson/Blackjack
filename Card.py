class Card:
    faces = ['ACE', '2', '3', '4', '5', '6', '7', '8', '9', '10', 'JACK', 'QUEEN', 'KING']
    suits = ['SPADES', 'DIAMONDS', 'HEARTS', 'CLUBS']
    def __init__(self, face, suit):
        self.face = face
        self.suit = suit

    def get_face(self):
        return self.face
    def get_suit(self):
        return self.suit
    def prettify(self):
        return self.face + " of " + self.suit
